import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const outputPath = path.join(root, 'eve-agent', 'agent', 'lib', 'social-data.ts')

function readJson(relativePath) {
  return JSON.parse(fs.readFileSync(path.join(root, relativePath), 'utf8'))
}

const snapshot = {
  generatedAt: new Date().toISOString(),
  summary: readJson('social/summary.json'),
  agents: readJson('social/agents.json'),
  threads: readJson('social/threads.json'),
  proposals: readJson('social/proposals.json'),
}

fs.mkdirSync(path.dirname(outputPath), { recursive: true })
fs.writeFileSync(
  outputPath,
  `/* This file is generated by scripts/sync-eve-agent-data.mjs. */\nexport const socialData = ${JSON.stringify(snapshot, null, 2)} as const\n`,
  'utf8',
)
console.log(`Synced Eve agent social data to ${path.relative(root, outputPath)}.`)
